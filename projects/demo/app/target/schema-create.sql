
    create table CAR (
        ID BIGINT generated by default as identity (start with 1),
        LOAD DOUBLE not null,
        primary key (ID)
    );

    create table CONTACT (
        ID BIGINT generated by default as identity (start with 1),
        CODE_CONTACT VARCHAR(256) not null,
        CUSTOMER_FK BIGINT not null,
        primary key (ID)
    );

    create table CUSTOMER (
        ID BIGINT generated by default as identity (start with 1),
        CUSTOMER_NAME VARCHAR(256) not null,
        CUST_ENUM VARCHAR(256) not null,
        primary key (ID)
    );

    create table ENUMERATION (
        ID BIGINT generated by default as identity (start with 1),
        FIELD_NAME VARCHAR(256) not null,
        ENUMERATION_TYPE VARCHAR(256) not null,
        primary key (ID)
    );

    create table ORDERD (
        ID BIGINT generated by default as identity (start with 1),
        CUSTOMER_ORDER_NO VARCHAR(256),
        CONTRACT_NO VARCHAR(256),
        CONVEYANCE_ENUM VARCHAR(256) not null,
        CARRY_TYPE_ENUM VARCHAR(256),
        SHIPPER_REMARK VARCHAR(256),
        CARRIER_REMARK VARCHAR(256),
        LEVEL_ENUM VARCHAR(256),
        CUSTOMER_TYPE_ENUM VARCHAR(256),
        REFER_TIME DATETIME,
        LINK_MAN VARCHAR(256),
        TEL VARCHAR(256),
        BOOK_TIME DATETIME not null,
        COMPLETE_TIME DATETIME,
        REMARK VARCHAR(256),
        CUSTOMER_FK BIGINT not null,
        primary key (ID)
    );

    create table ORDER_ROWD (
        ID BIGINT generated by default as identity (start with 1),
        PRODUCT_NUM DOUBLE not null,
        UNIT_NUM DOUBLE not null,
        GOODS_SUTTLE DOUBLE not null,
        GOODS_WEIGHT DOUBLE not null,
        GOODS_VOLUME DOUBLE not null,
        LABEL VARCHAR(256),
        CARRIAGE DOUBLE not null,
        ESPECIAL VARCHAR(256),
        GOODS_STATE VARCHAR(256),
        REMARK VARCHAR(256),
        ORDER_FK BIGINT not null,
        PRODUCT_FK BIGINT not null,
        primary key (ID)
    );

    create table ORDER_ROW_TASK (
        ID BIGINT generated by default as identity (start with 1),
        ORDER_ROW_FK BIGINT not null,
        TASK_FK BIGINT not null,
        primary key (ID)
    );

    create table ORGA (
        ID BIGINT generated by default as identity (start with 1),
        ORGA_NAME VARCHAR(256) not null,
        ORGA_CODE VARCHAR(256) not null,
        SNO VARCHAR(256),
        REMARK VARCHAR(256),
        PARENT_ID BIGINT not null,
        primary key (ID)
    );

    create table ORGA_PEOPLES (
        ID BIGINT generated by default as identity (start with 1),
        ORGA_FK BIGINT not null,
        PEOPLE_FK BIGINT not null,
        primary key (ID)
    );

    create table PAYMENT (
        ID BIGINT generated by default as identity (start with 1),
        NEW_ATTR1 VARCHAR(256) not null,
        NEW_ATTR2 VARCHAR(256) not null,
        PAYMENT_FK BIGINT not null,
        primary key (ID)
    );

    create table PEOPLES (
        ID BIGINT generated by default as identity (start with 1),
        PEOPLE_NAME VARCHAR(256) not null,
        PEOPLE_PASSWORD VARCHAR(256),
        PEOPLE_STATUS VARCHAR(256),
        BIRTH_DAY DATETIME,
        SEX VARCHAR(256) not null,
        BIRTH_PLACE VARCHAR(256),
        ADDRESS VARCHAR(256),
        TEL VARCHAR(256),
        ENGAGE_MODE VARCHAR(256),
        ENGAGE_DATE DATETIME,
        GRADE_ENUM VARCHAR(256),
        REMARK VARCHAR(256),
        primary key (ID)
    );

    create table PROD (
        ID BIGINT generated by default as identity (start with 1),
        PROD_NAME VARCHAR(256) not null,
        CODE VARCHAR(256),
        PRICE DOUBLE,
        DATEF DATETIME,
        PROD_TYPE VARCHAR(256),
        PROD_CUST_FK BIGINT not null,
        PRINCIPAL_FK BIGINT not null,
        primary key (ID)
    );

    create table TASK (
        ID BIGINT generated by default as identity (start with 1),
        NUM DOUBLE not null,
        VOLUMNE DOUBLE not null,
        REMARK VARCHAR(256),
        TASK_FORM_FK BIGINT not null,
        primary key (ID)
    );

    create table TASK_FORM (
        ID BIGINT generated by default as identity (start with 1),
        SET_DATE DATETIME not null,
        FORM_STATE VARCHAR(256) not null,
        REMARK VARCHAR(256),
        CAR_FK BIGINT not null,
        primary key (ID)
    );

